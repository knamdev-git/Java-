import java.util.Scanner ;

public class datatype_prob1 {
  public static void main(String[] args) {
  
   //Implicit datatype : It means the type conversion is automatically happen by the compiler or Computer ]
   byte b = 2;  // Byte size = 2 
   int a = b ; // int size = 4 

   // int <-- byte  , here byte can be converted into integer because integers range is higher than byte 
   System.out.println("Int implicit converted Byte value to Integer "+a);

  
  } 
}
